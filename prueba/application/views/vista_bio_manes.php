										<p ><b>Facundo Manes</b> es un neurólogo y neurocientífico argentino (acceda al resumen del Currículum Vitae). Vivió su infancia y adolescencia en Arroyo Dulce y Salto, en la provincia de Buenos Aires. Estudió en la Facultad de Medicina de la Universidad de Buenos Aires, donde se graduó en 1992 y, luego, en la Universidad de Cambridge, Inglaterra (PhD in Sciences). Una vez concluida su formación de posgrado en el exterior (Estados Unidos e Inglaterra) regresó al país con el firme compromiso de desarrollar recursos locales a fin de mejorar los estándares clínicos y de investigación en neurociencias cognitivas y neuropsiquiatría. Es rector de la Universidad Favaloro. Creó y dirige actualmente INECO (Instituto de Neurología Cognitiva) y el Instituto de Neurociencias de la Fundación Favaloro. Ambos centros son considerados hoy instituciones de vanguardia que lideran el campo de las neurociencias en América Latina, convirtiéndose en centros de referencia internacional para la investigación en neurociencias cognitivas y neuropsiquiatria. 
										</p>
										<div class="vs"></div>
										<p >Es Presidente de la Fundación INECO para la investigación en neurociencias. También preside la World Federation of Neurology Research Group on Aphasia, Dementia and Cognitive Disorders. Es profesor de Neurología y Neurociencias Cognitivas de la Facultad de Medicina y de Psicología de la Universidad Favaloro y de Psicología Experimental en University of South Carolina, EE. UU. Es profesor visitante del Departamento de Neurología de University of California San Francisco (EE. UU) y de Macquarie University (Sydney, Australia). Es investigador del Consejo Nacional de Investigaciones Científicas y Técnicas (CONICET), Argentina y del Australian Research Council (ACR) Centre of Excellence in Cognition and its Disorders.</p>
										<div class="vs"></div>
										<p >Ha publicado mas de 190 trabajos científicos originales en las más prestigiosas revistas internacionales de su especialidad como Brain, The Lancet Neurology, JAMA Neurology y Nature Neuroscience. El Dr. Manes es permanentemente invitado a dar conferencias en distintas partes del mundo y ha participado en prestigiosos foros científicos internacionales como la “Royal Society of Medicine” (Londres) y la “New York Academy of Sciences”, entre otros. <p/>  
										<div class="vs"></div>
										<p >Su área actual de investigación es la neurobiología de los procesos mentales. Entre sus contribuciones más importantes, el Dr. Manes identificó las áreas prefrontales relacionadas con el proceso de toma de decisiones en humanos, los mecanismos neurales de la agresión, en conjunto con el Dr Calder, y el rol de la ínsula en los procesos cognitivos y emocionales. Con el profesor Hodges y el profesor Zeman caracterizó un nuevo síndrome denominado Amnesia Epiléptica Transitoria. Además, junto con el Dr Bekinschtein, describió, por primera vez, el procesamiento emocional de pacientes con deterioro de conciencia mínima y, junto con el Dr Jeffrey Max, las áreas cerebrales involucradas en el desarrollo de los síntomas del déficit de atención e hiperactividad (ADHD). Recientemente con la Lic Torralva desarrollo de una batería neuropsicológica para detectar en forma temprana la demencia frontotemporal y con el Dr Ibáñez ha propuesto una teoría sobre el rol del contexto en los trastornos neuropsiquiátricos. Ha co-desarrollado una de las primeras unidades de rehabilitación cognitiva holística en América Latina. Su área actual de investigación es la neurobiología de los procesos mentales, particularmente los mecanismos neurales involucrados en la toma de decisiones y la conducta social. Su equipo ha trabajado intensamente en el desarrollo de baterías cognitivas y tests de screening destinados a facilitar el diagnóstico temprano de las demencias. </p>
